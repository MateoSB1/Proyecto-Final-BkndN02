class ProductDTO {
    constructor(id, title, description, price, code, status, stock, category, thumbnails, createdAt, updatedAt, __v) {
        this._id = id
        this.title = title
        this.description = description
        this.price = price
        this.code = code
        this.status = status
        this.stock = stock
        this.category = category
        this.thumbnails = thumbnails
        this.createdAt = createdAt
        this.updatedAt = updatedAt
        this.__v = __v
    }
}

export default ProductDTO 